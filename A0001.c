// C Token -> Smallest unit of C Program
#include<stdio.h>
int main()
{
	int i=5, j=7, k;
	k = i + j;
	printf("K = %d",k*3.14);
	return 0;
}
/*
1) Keywords : int, char, return -> 32 Keywords
2) Identifier : Variable = {i, j, k} function = {printf, main}
3) Operator : +, =, 
4) Constant : a) int -> 5, 7, 0   b) char ->     c) double -> 3.14
5) String : "K = %d"
6) Special Symbols : (, ), , ; # < > { }
*/
